CREATE TABLE EMPLOYEE (
  employee_id SMALLINT NOT NULL PRIMARY KEY,
  last_name VARCHAR(15),
  first_name VARCHAR(15),
  middle_initial VARCHAR(1),
  manager_id SMALLINT,
  job_id SMALLINT,
  hire_date DATETIME,
  salary NUMERIC(7,2),
  commission NUMERIC(7,2),
  department_id SMALLINT)
COMMENT = 'employee_id	Код сотрудника
last_name	Фамилия
first_name	Имя
middle_initial	Средний инициал
manager_id	Код начальника
job_id	Код должности
hire_date	Дата поступления в фирму
salary	Зарплата
commission	Комиссионные
department_id	Код отдела';

CREATE TABLE DEPARTMENT (
  department_id SMALLINT NOT NULL PRIMARY KEY,
  name VARCHAR(14),
  location_id SMALLINT )
COMMENT = ' department_id	Код отдела
name	Название отдела
location_id	Код места размещения
';

CREATE TABLE LOCATION (
  location_id SMALLINT NOT NULL PRIMARY KEY,
  regional_group VARCHAR(20) )
  COMMENT = ' location_id	Код места размещения
regional_group	Город
';

CREATE TABLE JOB  (
  job_id SMALLINT NOT NULL PRIMARY KEY,
  functn VARCHAR(30) )
  COMMENT = 'job_id	Код должности
function	Название должности
 ';

CREATE TABLE CUSTOMER (
  customer_id INTEGER NOT NULL PRIMARY KEY,
  name VARCHAR(45),
  address VARCHAR(40),
  city VARCHAR(30),
  state VARCHAR(2),
  zip_code VARCHAR(9),
  area_code SMALLINT,
  phone_number INTEGER,
  salesperson_id SMALLINT,
  credit_limit NUMERIC(9,2), 
  comments VARCHAR(500) )
  COMMENT = ' customer_id	Код покупателя
name	Название покупателя
address	Адрес
city	Город
state	Штат
zip_code	Почтовый код
area_code	Код региона
phone_number	Телефон
salesperson_id	Код сотрудника-продавца, обслуживаю-щего данного покупателя
credit_limit	Кредит для покупателя
comments	Примечания
';

CREATE TABLE SALES_ORDER (
  order_id SMALLINT NOT NULL PRIMARY KEY,
  order_date DATETIME,
  customer_id INTEGER,
  ship_date DATETIME,
  total NUMERIC(8,2) )
  COMMENT = 'order_id	Код договора
order_date	Дата договора
customer_id	Код покупателя
ship_date	Дата поставки
total	Общая сумма договора
 ';

CREATE TABLE ITEM (
  order_id SMALLINT NOT NULL,
  item_id SMALLINT NOT NULL,
  product_id INTEGER,
  actual_price NUMERIC(8,2),
  quantity INTEGER,
  total NUMERIC(8,2),
PRIMARY KEY (order_id,item_id)
)
COMMENT = ' order_id	Код договора, в состав которого входит акт
item_id	Код акта
product_id	Код продукта
actual_price	Цена продажи
quantity	Количество
total	Общая сумма
';

CREATE TABLE PRODUCT (
  product_id INTEGER NOT NULL PRIMARY KEY,
  description VARCHAR(100) )
  COMMENT = ' product_id	Код продукта
description	Название продукта
';

CREATE TABLE PRICE (
  product_id INTEGER NOT NULL,
  list_price NUMERIC(8,2),
  min_price NUMERIC(8,2),
  start_date DATETIME NOT NULL,
  end_date DATETIME,
  PRIMARY KEY (product_id,start_date)
)
COMMENT = 'product_id	Код продукта
list_price	Объявленная цена
min_price	Минимально возможная цена
start_date	Дата установления цены
end_date	Дата отмены цены
 ';

